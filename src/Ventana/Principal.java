    private void btn_IgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IgualActionPerformed
        Double resultado;
        String entero;
        try {
            number[1] = Double.parseDouble(Jlabel_Ventana.getText());

            if (divi == true) {
                if (number[1] == 0) {
                    Jlabel_Ventana.setText("Error");
                } else {
                    resultado = number[0] / number[1];
                if(resultado % 1 == 0){
                    entero = Double.toString(resultado);
                    Jlabel_Ventana.setText(entero.substring(0, entero.length() - 2));
                }else{
                         Jlabel_Ventana.setText(Double.toString(resultado));
                    }
                    
                }
                

            } else if (multi == true) {
                resultado = number[0] * number[1];
                if(resultado % 1 == 0){
                    entero = Double.toString(resultado);
                    Jlabel_Ventana.setText(entero.substring(0, entero.length() - 2));
                }else{
                         Jlabel_Ventana.setText(Double.toString(resultado));
                    }
            } else if (resta == true) {
                resultado = number[0] - number[1];
                if(resultado % 1 == 0){
                    entero = Double.toString(resultado);
                    Jlabel_Ventana.setText(entero.substring(0, entero.length() - 2));
                }else{
                         Jlabel_Ventana.setText(Double.toString(resultado));
                    }
            } else if (suma == true) {
                resultado = number[0] + number[1];
               if(resultado % 1 == 0){
                    entero = Double.toString(resultado);
                    Jlabel_Ventana.setText(entero.substring(0, entero.length() - 2));
                }else{
                         Jlabel_Ventana.setText(Double.toString(resultado));
                    }
            }else if(porcentaje = true){
                resultado = number[0] * (number[0]/100.0);
                if(resultado % 1 == 0){
                    entero = Double.toString(resultado);
                    Jlabel_Ventana.setText(entero.substring(0, entero.length() - 2));
                }else{
                         Jlabel_Ventana.setText(Double.toString(resultado));
                    }
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btn_IgualActionPerformed

